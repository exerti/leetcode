package dp;



/**
 * 
 * 
 * 动态规划的通用思考步骤
步骤 1：明确"阶段"

这题的阶段是"天数"，从第 0 天到第 n-1 天
步骤 2：找出"状态"

状态要能完整描述当前情况，影响后续决策
这题需要两个维度：天数 + 是否持有股票
步骤 3：列举"决策"

持有股票时：卖出 or 继续持有
不持有股票时：买入 or 继续不持有
步骤 4：写出"状态转移方程"

从"上一个状态"如何转移到"当前状态"

dp[i][0] = max(dp[i-1][0], dp[i-1][1] + prices[i])

要么昨天就不持有
要么昨天持有今天卖出
dp[i][1] = max(dp[i-1][1], dp[i-1][0] - prices[i])

要么昨天就持有
要么昨天不持有今天买入
步骤 5：确定"边界条件"

第 0 天的初始状态
步骤 6：确定"计算顺序"

从前往后计算，因为后面依赖前面
这种思考方式的优势
系统化：不用靠灵感，按步骤分析就能得出答案
可扩展：如果题目改成"最多交易 k 次"，只需增加一个维度 dp[i][k][0/1]
易验证：每个状态转移都有明确的含义，容易检查对错
这就是为什么要这样思考——它把复杂问题分解成了清晰的状态和转移，让问题变得可解。


 */
public class maxProfitSolution {
    public int maxProfit(int[] prices) {
        int n = prices.length;
        int[][] dp = new int[n][2];

        dp[0][0] = 0;
        dp[0][1] = -prices[0];

        for (int i = 1; i < n; i++) {
            dp[i][0] = Math.max(dp[i-1][0], dp[i-1][1] + prices[i]);
            dp[i][1] = Math.max(dp[i-1][1], dp[i-1][0] - prices[i]);
        }

        return dp[n-1][0];
    }
}
